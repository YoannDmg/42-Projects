#include "libft.h"

char	*ft_strdup(const char *s)
{
	char	*a;
	int		i;

	i = 0;
	while (s[i])
		i++;
	if ((a = ft_memalloc(i + 1)) == 0)
		return (NULL);
	i = -1;
	while (s[++i])
		a[i] = s[i];
	a[i] = '\0';
	return (a);
}
