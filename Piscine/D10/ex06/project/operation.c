#include "../includes/header.h"

int		ft_plus(int nb1, int nb2)
{
	return (nb1 + nb2);
}

int		ft_moins(int nb1, int nb2)
{
	return (nb1 - nb2);
}

int		ft_div(int nb1, int nb2)
{
	return (nb1 / nb2);
}

int		ft_mod(int nb1, int nb2)
{
	return (nb1 % nb2);
}

int		ft_mul(int nb1, int nb2)
{
	return (nb1 * nb2);
}
