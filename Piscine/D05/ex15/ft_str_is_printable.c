int		ft_str_is_printable(char *str)
{
	int i;
	int is_print;

	is_print = 0;
	i = 0;
	if (str[i] == '\0')
		return (1);
	while (str[i] != '\0')
	{
		if (str[i] >= 32 && str[i] <= 126)
			is_print = 1;
		else
			return (0);
		i++;
	}
	return (is_print);
}
