char	*ft_strrev(char *str)
{
	char	c;
	int		i;
	int		a;

	i = 0;
	a = 0;
	while (str[i] != '\0')
	{
		i++;
	}
	i--;
	while (i >= a)
	{
		c = str[a];
		str[a] = str[i];
		str[i] = c;
		a++;
		i--;
	}
	return (str);
}
